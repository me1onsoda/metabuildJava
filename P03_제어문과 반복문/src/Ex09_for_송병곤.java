
public class Ex09_for_송병곤 {

	public static void main(String[] args) {
		for(int i=1; i<6; i++) {
			for (int j=0; j<i; j++) {
				System.out.print("*");				
			}
			System.out.print("\n");
		}
		
		System.out.print("\n");
		
		for(int k=5; k>0; k--) {
			for (int j=0; j<k; j++) {
				System.out.print("*");				
			}
			System.out.print("\n");
		}
		
	}

}
